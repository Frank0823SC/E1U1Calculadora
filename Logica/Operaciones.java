package Logica;

public class Operaciones {
        
    public int suma (int n1, int n2){
        int resul = 0;
        resul = n1 + n2;
        return resul;
    }
    
    public int resta (int n1, int n2){
        int resul = 0;
        resul = n1 - n2;
        return resul;
    }
    
    public int multiplicacion (int n1, int n2){
        int resul = 0;
        resul = n1 * n2;
        return resul;
        
    }
    
    public double division (double n1, double n2){
        double resul = 0; 
        resul = n1 / n2;
        return resul;
    }
    
}

